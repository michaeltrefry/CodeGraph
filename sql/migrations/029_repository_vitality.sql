    velocity_prior_6_months INT NOT NULL DEFAULT 0,
    velocity_change_percent DOUBLE NOT NULL DEFAULT 0,
    dormant_months_12m INT NOT NULL DEFAULT 0,
    max_inactive_streak_months INT NOT NULL DEFAULT 0,
