    firefighting_rate_90d DOUBLE NOT NULL DEFAULT 0,
    firefighting_rate_365d DOUBLE NOT NULL DEFAULT 0,
    firefighting_authors_90d INT NOT NULL DEFAULT 0,
    computed_at DATETIME NOT NULL,
